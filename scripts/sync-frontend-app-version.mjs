import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const workspaceRoot = process.cwd();
const packageJsonPath = resolve(workspaceRoot, 'package.json');
const outputPath = resolve(workspaceRoot, 'apps/frontend/src/environments/app-version.ts');
const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf8'));

if (typeof packageJson.version !== 'string' || packageJson.version.trim() === '') {
  throw new Error('package.json version must be a non-empty string.');
}

const versionLiteral = `'${packageJson.version.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
const content = [
  '// Generated by scripts/sync-frontend-app-version.mjs from package.json.',
  `export const appVersion = ${versionLiteral};`,
  ''
].join('\n');

writeFileSync(outputPath, content);
